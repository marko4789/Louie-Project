    class Logica {
        public int[] getNumeros() {

            int[] numbers = new int[12];
            int count = 0;

            while(count < 12) {
                Random r = new Random();
                int pares = r.nextInt(6) + 1;
                int nvr = 0;

                for (int i = 0; i < 12; i++) {
                    if(numbers[i] == pares) {
                        nvr++;
                    }
                }
                if(nvr < 2) {
                    numbers[count] = pares;
                    count++;
                }//fin

            }


            return numbers;
        }
    }

